    private void DFSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DFSActionPerformed
        
    // Acción Botón (DFS).
    
        ListWarehouse list = GlobalVariables.getListWarehouse();
        
        AdjacencyMatrix graph = new AdjacencyMatrix(list.getLength());
        graph.createMatrix(list);
        

        NodeWarehouse pointer5 = list.getHead();
        SimpleListWithArray list1 = new SimpleListWithArray(list.getLength());
//        
        while (pointer5 != null){
            graph.addVertax(pointer5.getElement().getName());
            list1.addAtTheEnd(pointer5.getElement().getName());
            pointer5 = pointer5.getNext();
        }
       
        GlobalVariables.setDfs(graph.listProductsDFS(graph, list1, list));
        
        Reporte.setText(GlobalVariables.getDfs());
        
    // Retorna Acción de DFS.
    }//GEN-LAST:event_DFSActionPerformed

