    private void CaracteristicasAlmacenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CaracteristicasAlmacenActionPerformed
        
    // Mostrar Caracteristicas Del Almacen A Crear.
    
        boolean nombreexiste = false;
        for (int i = 0; i < almacenes.length; i++) {
            if (NombreAlmacen.getText().equals(almacenes[i])) {
                nombreexiste = true;
                break;
            }            
        } 
        if (NombreAlmacen.getText().length() < 2 && NombreAlmacen.getText().matches("^[a-zA-Z]*$")) {
            if (NombreAlmacen.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Error, no agrego nada");
                this.setVisible(false);
            }
            else if (!nombreexiste) {
                    WarehouseCharacteristics wc = new WarehouseCharacteristics();
                    newwarehousename = NombreAlmacen.getText();
                    this.setVisible(false);
                    this.setDefaultCloseOperation(this.DISPOSE_ON_CLOSE);
                    wc.setVisible(true);
                    ListProducts listpro = new ListProducts(null);
                    Warehouse newWarehouse = new Warehouse(newwarehousename, listpro);
                    ListWarehouse list = GlobalVariables.getListWarehouse();
                    list.insertEnd(newWarehouse);
                    list.printList();
                    System.out.println(list.getLast().getName());
             
            }
            else{
                JOptionPane.showMessageDialog(null, "Error, el almacen ya existe");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Error, ingrese una letra del abecedario");
        }
    
    // Retorna Error Si Ya Existe Un Almacen Parecido y/o Lo Guarda.
    
    }//GEN-LAST:event_CaracteristicasAlmacenActionPerformed

