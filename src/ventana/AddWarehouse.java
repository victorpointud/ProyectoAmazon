    public String[] AllWarehouses(ListWarehouse listWarehouse) {
        
    // Todos Los Almacenes.
    
        String[] almacenes = new String[listWarehouse.getLength()];
        NodeWarehouse pointer = listWarehouse.getHead();
        int index = 0;
        while (pointer != null) {
            String warehouseLetter = pointer.getElement().getName();
            almacenes[index] = warehouseLetter;
            index++;
            pointer = pointer.getNext();
        }
        return almacenes;
    
    // Retorna Almacenes.
    }
    
