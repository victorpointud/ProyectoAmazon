    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
    // Acción Del Botón 1 (Añadir).
    
        String newproductname = NameProduct.getText();
        int newproductqty = (Integer) jSpinner1.getValue();
        String warehouseLetter = String.valueOf(PickWarehouse.getSelectedItem());
        
        ListWarehouse list = GlobalVariables.getListWarehouse();
        NodeWarehouse pointer = list.getHead();
        boolean keepGoing = true;
        while (pointer != null && keepGoing) {
            if (warehouseLetter.equals(pointer.getElement().getName())) {
                keepGoing = false;
                Product newproduct = new Product(newproductname, newproductqty);
                NodeProduct newnodeproduct = new NodeProduct(newproduct);
                pointer.getElement().getListOfProducts().AddProduct(newnodeproduct);
            }
            pointer = pointer.getNext();
        }
    
    // Retorna El Elemento Añadido A La Lista De Productos.
    }//GEN-LAST:event_jButton1ActionPerformed

